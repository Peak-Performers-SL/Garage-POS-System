    private void txtJobIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJobIDKeyPressed
        // Press Enter Key
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String JobID = txtJobID.getText();
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rustrepairpos", "root", "");
                PreparedStatement pst = con.prepareStatement ("select * from Jobs where JobID = ?");
                
                pst.setString(1, JobID);
                rs = pst.executeQuery();
                
                if (rs.next() == true) {
                    String RepairCategory = rs.getString("RepairCategory");
                    String EmpName = rs.getString("EmpName");
                    String CustomerName = rs.getString("CustomerName");
                    String CustomerTelephone = rs.getString("CustomerTelephone");
                    String CustomerAddress = rs.getString("CustomerAddress");
                    String VehicleNo = rs.getString("VehicleNo");
                    String RepairDate = rs.getString("RDate");
                    String RepairCharge = rs.getString("RCharge");
                    
                    txtRepairJCategory.setSelectedItem(RepairCategory.trim());
                    txtEmpName.setText(EmpName.trim());
                    txtCustomerName.setText(CustomerName.trim());
                    txtCustomerTelephone.setText(CustomerTelephone.trim());
                    txtCustomerAddress.setText(CustomerAddress.trim());
                    txtVehicleNo.setText(VehicleNo.trim());
                    txtRepairDate.setText(RepairDate.trim());
                    txtRepairCharge.setText(RepairCharge.trim());
                    
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(RestorationJobs.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(RestorationJobs.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_txtJobIDKeyPressed

